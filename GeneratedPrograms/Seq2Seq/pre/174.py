def main():
	n, m = map(int, input().split())
if n < m :
		print(- 1)
return
for _ in range(n):
		x, y = map(int, input().split())
if x < y :
			break
else :
			x = x + y
if x < y :
			print(- 1)
return
if x < 0 :
			print(- 1)
return
if x < 0 :
			break
else :
			x = x + y
print(x)
if __name__ == '__main__' :
	main()