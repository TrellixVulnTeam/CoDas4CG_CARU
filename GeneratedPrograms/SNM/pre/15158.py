t = int(input())
for i in range(t):
    a, b = map(int, input().split())
    if a == 0:
        print(-1)
    elif a == b:
        print(a - b + 1)
    elif a == 0:
        print(a - b - 1)
    elif a == 0:
        print(a - b - 1)
    elif a == 0:
        print(a - b - 1)
    elif a == 0:
        print(a - b - 1)
    else:
        print(a - b + 1)
